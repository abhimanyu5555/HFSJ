package chap8.bean;

public class Person {

	private String name;
	private int impId;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getImpId() {
		return impId;
	}
	public void setImpId(int impId) {
		this.impId = impId;
	}
	
}
